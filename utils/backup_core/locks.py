"""Защита от гонки при одновременных операциях с бэкапом одного сервера.

Без этого, например, /save и /load запущенные почти одновременно на одном
сервере могли пересекаться: запись нового файла бэкапа во время того, как
другая корутина читает/удаляет файлы при ретеншне, в худшем случае даёт
частично прочитанный (повреждённый) gzip-поток. Сам Discord API это не
защищает — это чисто файловая гонка на диске бота.

asyncio.Lock, а не threading.Lock: весь бот асинхronный и однопоточный
(стандартная модель discord.py) — конкурентность здесь между корутинами
в одном event loop, а не между потоками ОС."""

from __future__ import annotations

import asyncio
import collections

_guild_locks: dict[int, asyncio.Lock] = collections.defaultdict(asyncio.Lock)


def get_guild_lock(guild_id: int) -> asyncio.Lock:
    """Один Lock на guild_id, общий для всех вызовов в рамках процесса бота.
    defaultdict здесь безопасен: создание Lock() само по себе не требует
    блокировки, а нам не критично, если в редком случае гонки на старте
    создадутся два экземпляра Lock — каждый отдельный вызов всё равно
    дожидается своего же объекта целиком за время одной корутины."""
    return _guild_locks[guild_id]
